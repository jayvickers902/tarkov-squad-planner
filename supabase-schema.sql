-- Run this in the Supabase SQL editor (Dashboard → SQL Editor → New query)

create table public.parties (
  id         bigint generated by default as identity primary key,
  code       text unique not null,
  leader     text not null,
  map_id     text,
  map_name   text,
  map_norm   text,
  members    jsonb not null default '{}',
  spawn      text,
  created_at timestamptz default now()
);

-- Allow anyone to read/write parties (the 6-char code is the access control)
alter table public.parties enable row level security;

create policy "Public read" on public.parties
  for select using (true);

create policy "Public insert" on public.parties
  for insert with check (true);

create policy "Public update" on public.parties
  for update using (true);

